package extraWork;

public class Course {
	
	int id;
	String name;
	String detail;
	String instructor;
	double price;
	
	public Course() {}
	
	public Course(int id, String name, String detail, String instructor, double price) {
		this.id = id;
		this.name = name;
		this.detail = detail;
		this.instructor = instructor;
		this.price = price;
	}
	
}
